package corejava.eg1;

public class switchcase {
public static void main(String[] args) {
	int tech =1;
	
	switch (tech ) {
	case 1:{
		System.out.println("java");
		break;
	}
	case 2:{
		System.out.println("es2");
		break;
	}
	default :{
		System.out.println("not listed ");
	}
	}
}
}
